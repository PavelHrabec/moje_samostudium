
#Podmínky říkají programu, co má udělat, když něco platí.


cislo = int(input("Zadej číslo: "))

if cislo > 0:
    print("Číslo je kladné.")
elif cislo < 0:
    print("Číslo je záporné.")
else:
    print("Číslo je nula.")




#Co se tu děje:

#if → spustí se, když je podmínka pravdivá.

#elif → "jinak, pokud" – další možnost.

#else → pokud nic předtím neplatí.



